    public void addAutomaticMultaEnc(){
        try{
            Multa multa =mm.dia();
            Multa multa2 = mm.diaIn();
            if(multa.getFecha_aplicacion().before(multa2.getFecha_aplicacion()) && multa.getCed_usuario() == multa2.getCed_usuario()){
                if(mm.registrarEnc(multa)){
                    System.out.println("Se ha aplicado una multa a un usuario con el No de identificacion: " + multa.getCed_usuario());
                    addAutomaticMultaDet();
                    mm.noDup(multa);
                }else{
                    System.out.println("No se ha podido aplicar multa");
                }
            }
        }catch(Exception e){
            System.out.println("NO EXISTE NADIE CON UN ATRASO");
        }
    }
    
    public void addAutomaticMultaDet(){
        try{
            int id = mm.getIdEncMulta();
            ArrayList<Integer> idLib = mm.getIdLib(mm.dia().getId_multa());
            for(int i = 0; i < idLib.size(); i++){
                int idEjem = mm.getIdEjem(idLib.get(i));

                Detalle_Multa dm = new Detalle_Multa(id, idEjem);
                if (mm.addDet(dm)) {
                    System.out.println("Multa aplicada con exito");
                    ModeloMultaAutomatic.getTabla();
                    ModeloMultaAutomatic.Limpiar_Tabla();
                } else {
                    System.out.println("No se pudo aplicar la multa");
                }
            }
        }catch(Exception e){
            System.out.println("NO EXISTE NADIE CON UN ATRASO");
        }
    }
    
