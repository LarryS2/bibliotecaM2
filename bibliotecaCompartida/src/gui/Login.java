    private void txtuserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtuserMousePressed
        if(txtuser.getText().equals("Ingrese su cedula")){
            txtuser.setText("");
            txtuser.setForeground(Color.black);
        }
        if(String.valueOf(passworduser.getPassword()).isEmpty()){
            passworduser.setText("********");
            passworduser.setForeground(Color.gray);
        }
    }//GEN-LAST:event_txtuserMousePressed

    private void passworduserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passworduserMousePressed
        if(txtuser.getText().isEmpty()){
            txtuser.setText("Ingrese su cedula");
            txtuser.setForeground(Color.gray);
        }
        if(String.valueOf(passworduser.getPassword()).equals("********")){
            passworduser.setText("");
            passworduser.setForeground(Color.black);
        }
    }//GEN-LAST:event_passworduserMousePressed

    private void btnlabeliniciarsesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnlabeliniciarsesionMouseClicked
        multi = new Menu();
        multi_u = new Menu_Invitado();
        boolean ingreso = false;

        if(txtuser.getText().length()>0) {
            //if(passworduser.getPassword().length>0) {
                for (int i = 0; i < ControladorUsuario.listausuarios.size(); i++) {
                    if (txtuser.getText().equals(ControladorUsuario.listausuarios.get(i).getCedula())) {
                        String rol = ControladorUsuario.listausuarios.get(i).getTipo_usuario();
                        //String nombre = ControladorUsuario.listausuarios.get(i).getPrimer_nombre().toUpperCase();
                        //String apellido = ControladorUsuario.listausuarios.get(i).getPrimer_apellido().toUpperCase();
                        if (rol.equalsIgnoreCase("ADMINISTRADOR")) {
                            ingreso = true;
                            this.dispose();
                            multi.setLocationRelativeTo(null);
                           // multi.labelnombreuser.setText("BIEVENIDO, "+nombre+" "+apellido);
                            multi.setVisible(true);
                        } else {
                            if (rol.equalsIgnoreCase("INVITADO")) {
                                ingreso = true;
                                this.dispose();
                                multi_u.setLocationRelativeTo(null);
                                multi_u.setVisible(true);
                            }
                        }
                        i = ControladorUsuario.listausuarios.size();
                    }
                }
                if (ingreso == false) {
                    JOptionPane.showMessageDialog(null, "CONTRASEÑA O USUARIO INCORRECTO");
                }
            //}else {
            //    JOptionPane.showMessageDialog(null, "CAMPO DE CONTRASEÑA VACÍO");
            //}
        }else {
            JOptionPane.showMessageDialog(null, "CAMPO DE USUARIO VACÍO");
        }
    }//GEN-LAST:event_btnlabeliniciarsesionMouseClicked

